#operasi tambah +
a=10
b=3
hasil=a+b
print(a,'+',b,'=',hasil) 

#operasi kurang -
hasil=a-b
print(a,'-',b,'=',hasil) 

#operasi kali *
hasil=a*b
print(a,'*',b,'=',hasil) 

#operasi bagi /
hasil=a/b
print(a,'/',b,'=',hasil) 

#operasi eksponen (pangkat)**
hasil=a**b
print(a,'**',b,'=',hasil) 

#operasi modulus %
hasil=a%b
print(a,'%',b,'=',hasil) 

#operasi floor division //

hasil=a//b
print(a,'//',b,'=',hasil)  

